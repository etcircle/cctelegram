"""Execute transcript history pagination callbacks.

Core responsibilities:
  - Own CB_HISTORY_PREV and CB_HISTORY_NEXT execution.
  - Parse history byte-window callback payloads.
  - Re-check topic/window ownership before reading tmux history.

Key components:
  - execute_history_callback()
"""

from __future__ import annotations

from typing import Any

from cctelegram.handlers.callback_data import CB_HISTORY_NEXT, CB_HISTORY_PREV
from cctelegram.handlers.history import send_history
from cctelegram.handlers.message_sender import safe_edit

from . import (
    STALE_CALLBACK_TEXT,
    _answer_invalid_pending_picker_callback,
    _callback_window_is_current,
    _validate_pending_picker_callback,
    window_lease,
)


async def execute_history_callback(authorized: Any, adapters: Any) -> None:
    context = authorized.ctx.context
    user = authorized.ctx.user
    query = authorized.ctx.query
    data = authorized.command.data
    cb_thread_id = authorized.ctx.thread_id
    lease = window_lease(authorized, adapters)
    session_manager = adapters.session_manager
    tmux_manager = adapters.tmux_manager

    async def reject_stale_window_callback(window_id: str) -> bool:
        return await lease.reject_stale_window(window_id)

    async def reject_invalid_pending_picker(
        expected_states: tuple[str, ...],
        answer_text: str,
    ) -> tuple[bool, int | None]:
        ok, pending_tid, _reason = _validate_pending_picker_callback(
            context.user_data,
            cb_thread_id,
            expected_states,
        )
        if ok:
            return False, pending_tid
        await _answer_invalid_pending_picker_callback(query, answer_text)
        return True, pending_tid

    # History: older/newer pagination
    # Format: hp:<page>:<window_id>:<start>:<end> or hn:<page>:<window_id>:<start>:<end>
    if data.startswith(CB_HISTORY_PREV) or data.startswith(CB_HISTORY_NEXT):
        prefix_len = len(CB_HISTORY_PREV)  # same length for both
        rest = data[prefix_len:]
        try:
            parts = rest.split(":")
            if len(parts) < 4:
                # Old format without byte range: page:window_id
                offset_str, window_id = rest.split(":", 1)
                start_byte, end_byte = 0, 0
            else:
                # New format: page:window_id:start:end (window_id may contain colons)
                offset_str = parts[0]
                start_byte = int(parts[-2])
                end_byte = int(parts[-1])
                window_id = ":".join(parts[1:-2])
            offset = int(offset_str)
        except (ValueError, IndexError):
            await query.answer("Invalid data")
            return

        if not _callback_window_is_current(
            session_manager, user.id, cb_thread_id, window_id
        ):
            await query.answer(STALE_CALLBACK_TEXT, show_alert=True)
            return

        w = await tmux_manager.find_window_by_id(window_id)
        if w:
            await send_history(
                query,
                window_id,
                offset=offset,
                edit=True,
                start_byte=start_byte,
                end_byte=end_byte,
                # Don't pass user_id for pagination - offset update only on initial view
                # This prevents offset from going backwards if new messages arrive while paging
            )
        else:
            await safe_edit(query, "Window no longer exists.")
        await query.answer("Page updated")
